#include "libft.h"

size_t			ft_count(intmax_t n, short int base)
{
	size_t c;

	if (n >= 0)
		c = 1;
	else if (n < 0)
		c = 2;
	while (n / base != 0)
	{
		n = n / base;
		c++;
	}
	return (c);
}
